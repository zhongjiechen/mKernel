#!/bin/bash
set -euo pipefail

# End-to-end reproducible H200x4 release run:
#   1. IB/RDMA torch+NCCL baselines
#   2. mKernel sweeps

HERE=$(cd "$(dirname "$0")" && pwd)
REPO=$(dirname "$HERE")
mkdir -p "$HERE/results"

export WARMUP=${WARMUP:-3}
export ITERS=${ITERS:-10}
export TIMEOUT=${TIMEOUT:-3000}
export PORT_BASE=${PORT_BASE:-28000}

export BASELINE_NET=ib
export BASELINE_RING_ATTN_IMPL=${BASELINE_RING_ATTN_IMPL:-flash_all_gather}
export NPROC_PER_NODE=${NPROC_PER_NODE:-8}
export NCCL_NET_PLUGIN=${NCCL_NET_PLUGIN:-none}
export MKERNEL_BASELINE_NCCL_SOCKET_IFNAME=${MKERNEL_BASELINE_NCCL_SOCKET_IFNAME:-bond0,bond1,bond2,bond3,bond4,bond5,bond6,bond7}
export MKERNEL_BASELINE_NCCL_IB_HCA=${MKERNEL_BASELINE_NCCL_IB_HCA:-mlx5_bond_0,mlx5_bond_1,mlx5_bond_2,mlx5_bond_3,mlx5_bond_4,mlx5_bond_5,mlx5_bond_6,mlx5_bond_7}
export MKERNEL_BASELINE_NCCL_IB_GID_INDEX=${MKERNEL_BASELINE_NCCL_IB_GID_INDEX:-3}

export H200X4_DISPATCH_GEMM_SHAPES=${H200X4_DISPATCH_GEMM_SHAPES:-8192,16384,32768,65536,131072}
export H200X4_GEMM_RS_SHAPES=${H200X4_GEMM_RS_SHAPES:-4096,8192,16384,32768,65536}
export H200X4_AG_GEMM_SHAPES=${H200X4_AG_GEMM_SHAPES:-8192,16384,32768,49152}
export H200X4_GEMM_AR_SHAPES=${H200X4_GEMM_AR_SHAPES:-8192,12288,16384,20480,22528}
export H200X4_RING_ATTENTION_SHAPES=${H200X4_RING_ATTENTION_SHAPES:-768,1536,3072,6144,12288}

CONFIG_SNAPSHOT=${CONFIG_SNAPSHOT:-"$HERE/results/h200x4_release_config.env"}
cat > "$CONFIG_SNAPSHOT" <<EOF
# Generated by bench/run_h200x4_release.sh
WARMUP=$WARMUP
ITERS=$ITERS
TIMEOUT=$TIMEOUT
BASELINE_NET=$BASELINE_NET
BASELINE_RING_ATTN_IMPL=$BASELINE_RING_ATTN_IMPL
NPROC_PER_NODE=$NPROC_PER_NODE
NCCL_NET_PLUGIN=$NCCL_NET_PLUGIN
MKERNEL_BASELINE_NCCL_SOCKET_IFNAME=$MKERNEL_BASELINE_NCCL_SOCKET_IFNAME
MKERNEL_BASELINE_NCCL_IB_HCA=$MKERNEL_BASELINE_NCCL_IB_HCA
MKERNEL_BASELINE_NCCL_IB_GID_INDEX=$MKERNEL_BASELINE_NCCL_IB_GID_INDEX
PORT_BASE=$PORT_BASE
H200X4_DISPATCH_GEMM_SHAPES=$H200X4_DISPATCH_GEMM_SHAPES
H200X4_GEMM_RS_SHAPES=$H200X4_GEMM_RS_SHAPES
H200X4_AG_GEMM_SHAPES=$H200X4_AG_GEMM_SHAPES
H200X4_GEMM_AR_SHAPES=$H200X4_GEMM_AR_SHAPES
H200X4_RING_ATTENTION_SHAPES=$H200X4_RING_ATTENTION_SHAPES
GEMM_AR_INTER_SEND_SMS=${GEMM_AR_INTER_SEND_SMS:-20}
GEMM_AR_NUM_INTRA_COMM_SMS=${GEMM_AR_NUM_INTRA_COMM_SMS:-4}
MKERNEL_MAX_INFLIGHT=${MKERNEL_MAX_INFLIGHT:-64}
EOF
echo "==== wrote release config $CONFIG_SNAPSHOT ===="

run_baseline() {
    local kernel=$1
    local shapes=$2
    local port=$3
    echo "==== IB NCCL baseline ${kernel} shapes=${shapes} ===="
    MASTER_PORT=$port SHAPES=$shapes CLEAN_RESULTS=1 bash "$HERE/run_h200x4_baseline.sh" "$kernel"
}

run_baseline dispatch_gemm  "$H200X4_DISPATCH_GEMM_SHAPES" 27200
run_baseline gemm_rs        "$H200X4_GEMM_RS_SHAPES"        27220
run_baseline ag_gemm        "$H200X4_AG_GEMM_SHAPES"        27240
run_baseline gemm_ar        "$H200X4_GEMM_AR_SHAPES"        27260
run_baseline ring_attention "$H200X4_RING_ATTENTION_SHAPES" 27280

echo "==== mKernel h200x4 all ===="
PORT_BASE=$PORT_BASE bash "$HERE/run_h200x4_mkernel.sh" all
